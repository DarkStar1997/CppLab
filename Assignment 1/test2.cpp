//1 foot = 30.48 cm
//1 inch = 2.54 cm

#include <iostream>
double convert(const double &x, const double &y)
{
	return x * 30.48 + y * 2.54;
}
int main()
{
	double ft, inch;
	std::cout<<"Enter the distance in feet and inches\n";
	std::cin>>ft>>inch;
	std::cout<<"The converted distance in cm is: "<<convert(ft, inch)<<'\n';
}
